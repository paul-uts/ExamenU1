    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        // TODO add your handling code here:
          try {
        int id = Integer.parseInt(jtxtBusquedaidPaciente.getText().trim());
        pacienteActual = persistencia.buscarPacientePorID(id);
        if (pacienteActual != null) {
            jTextArea1.setText(pacienteActual.toString());
            txtIdPaciente.setText(String.valueOf(pacienteActual.getId()));
            txtNombrePaciente.setText(pacienteActual.getNombre());
            txtEdadPaciente.setText(String.valueOf(pacienteActual.getEdad()));
            txtDireccionPaciente.setText(pacienteActual.getDireccion());
        } else {
            JOptionPane.showMessageDialog(this, "Paciente no encontrado");
        }
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "ID inválido");
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Error al buscar paciente: " + e.getMessage());
    }
    }//GEN-LAST:event_btnBuscarActionPerformed


    private void btnAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAceptarActionPerformed
        // TODO add your handling code here:
           if (pacienteActual == null) {
        JOptionPane.showMessageDialog(this, "Primero busca un paciente.");
        return;
    }

    try {
        int nuevoId = Integer.parseInt(txtIdPaciente.getText().trim());
        String nuevoNombre = txtNombrePaciente.getText().trim();
        int nuevaEdad = Integer.parseInt(txtEdadPaciente.getText().trim());
        String nuevaDireccion = txtDireccionPaciente.getText().trim();

        Paciente actualizado = new Paciente(nuevoId, nuevoNombre, nuevaEdad, nuevaDireccion);
        persistencia.actualizarPaciente(actualizado);
        JOptionPane.showMessageDialog(this, "Paciente actualizado correctamente.");
        
        jTextArea1.setText("");
        pacienteActual = null;
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "Edad o ID inválidos.");
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Error al actualizar paciente: " + e.getMessage());
    }
    }//GEN-LAST:event_btnAceptarActionPerformed

